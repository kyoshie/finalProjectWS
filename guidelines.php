            <h1 class="text-center">Guidelines</h1>
            <h4 class="text-center">Kalinangan Referred Journal</h4>
            <p class="text-justify">The Kalinangan Refereed Journal is the official multidisciplinary research publication of the University of Batangas. It publishes all researches across disciplines to provide a common medium for knowledge and research dissemination in various fields of scientific, academic, and professional interests. </p>
            <p class="text-justify">The double-blind refereeing system is used by the Kalinangan Refereed Journal.  The Board of Reviewers (Editorial Board) for the Kalinangan Refereed Journal is created by the Director of RPO and maybe changed from time to time based on the research thrusts of the university. Three (3) referees review each article prior to publication: one (1) internal referee who is a member of the Board of Reviewers in a particular cluster/ discipline and two (2) external referees who are subject matter experts from other institutions and who are requested to critic and comment on the paper under review.</p>
            <h3 class="text-center">Research Agenda</h3>
            <p class="text-start">To further enhance RPO's research objectives, the following research agenda shall be observed:</p>
            <p class="text-start">1. Create a research-driven culture and environment among the members of the UB community and its stakeholders;</p>
            <p class="text-start">2. Respond to institutional, local, national, and international initiatives through multidisciplinary researches;</p>
            <p class="text-start">3. Produce high quality researches for publication; </p>
            <p class="text-start">4. Ensure the utilization and dissemination of relevant research outputs; </p>
            <p class="text-start">5. Support other functions of the University such as instruction, production, community extension, and marketing services; </p>
            <p class="text-start">6. Seek external support from both national and international agencies; </p>
            <p class="text-start"> 7. Engage in research and other different modes of academic collaboration.</p>
            <h3 class="text-center">Project Proposal Procedural Steps</h3>
            <h4 class="text-start">1. Call for Paper Call</h4>
            <p class="text-justify">for research proposal is sent out by the Office usually at the start of the semester. The memorandum is coursed through the deans of the different colleges or heads of offices. The invitation to submit a research proposal comes with it the rubrics for rating the proposal as well as the research proposal format.  
            </p>
            <h4 class="text-start">2. Screening / Review</h4>
            <p class="text-justify">Three copies of the proposal paper shall be submitted for screening. The screening is initially done by the RPO Director together with at least two of the designated internal panel of reviewers from the discipline. The RPO Director may deem it necessary to call on external reviewers for assistance in some necessary cases.  </p>
            <h4 class="text-start">3. Approval and Contract Signing </h4>
            <p class="text-justify">Final approval of the proposal will be by the University President after the RPO Director has endorsed it for implementation and funding. The researchers are then notified of the approval of their research proposal and they will be required to sign the agreement on research project.</p>
            <h3 class="text-center">Budget Allocation</h3>
            <p class="text-justify">Budget allocation is computed based on the suggested budget details or needs. The allotted budget for the research may be claimed by the researchers quarterly or as may be deemed necessary by the researcher/s. The researcher/s should write a memo requesting for budget to the University’s Budget Management Officer as endorsed by the RPO Director.</p>
            <h3 class="text-center">Research Proper and Time Frame</h3>
            <p class="text-justify">The researcher/s will be given one-year to complete the research with ten months for the actual research while the remaining two months will be allotted for refereeing, revision and eventual publication in the journal.</p>
            <h3 class="text-center">Publication Procedures</h3>
            <p class="text-start">After completion of the research project, the author endorses the paper to RPO for review. </p>
            <p class="text-start">The following are the procedures to be followed:  </p>
            <p class="text-justify">STAGE 1: The internal referee (who reviews according to expertise) makes the first round of content review of paper after it has undergone anti-plagiarism, grammarian, and other tests. The paper under review shall adhere to the format prescribed by RPO.</p>
            <p class="text-start">STAGE 2: Author revises the paper following the internal referee’s comments and suggestions.</p>
            <p class="text-start">STAGE 3: Internal referee reviews the revised paper. </p>
            <p class="text-start">STAGE 4: The internal referee endorses the revised paper to the Editor-in-chief.</p>
            <p class="text-start">STAGE 5: Editor- in- chief sends the paper to the External Referee/s. </p>
            <p class="text-start">STAGE 6: External Referee reviews the paper using the refereeing form.</p>
            <p class="text-start">STAGE 7: Editor-in- chief retrieves peer-reviewed paper (from external referees).</p>
            <p class="text-start">STAGE 8: Author reviews the paper again following the referee’s comments and suggestions. </p>
            <p class="text-start">STAGE 9: Editor-in- chief resends the paper to the concerned referee for another review if needed. </p>
            <p class="text-start">STAGE 10: Editor-in-chief does the final round of the paper review.</p>
            <p class="ending text-start">STAGE 11: Editorial Office gives the dummy and the final copy of the journal to the printer/ publisher.</p>
            
